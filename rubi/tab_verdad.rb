# Declaración de variables booleanas
a = true
b = false

# Operación lógica AND (&&) entre 'a' y 'b'
puts a && b  # false

# Declaración de variables enteras
a_num = 2
b_num = 3
c = 4
d = 10

# Evaluación de condiciones lógicas
puts a_num == b_num && c < d          # false
puts !(a_num == b_num) && c < d       # true